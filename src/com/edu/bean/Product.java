package com.edu.bean;

public class Product {
	private String id ;
	private String title ;
	private String pic ;
	private int stock ;
	private double price ;
	private String code ;
	private int category_id ;
	private int brand_id ;
	private int pro_price ;
	private int hot_recommand;
	private int product_off ;
	private String product_up_date ;
	private String product_off_date ;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getPic() {
		return pic;
	}
	public void setPic(String pic) {
		this.pic = pic;
	}
	public int getStock() {
		return stock;
	}
	public void setStock(int stock) {
		this.stock = stock;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public int getCategory_id() {
		return category_id;
	}
	public void setCategory_id(int category_id) {
		this.category_id = category_id;
	}
	public int getBrand_id() {
		return brand_id;
	}
	public void setBrand_id(int brand_id) {
		this.brand_id = brand_id;
	}
	public int getPro_price() {
		return pro_price;
	}
	public void setPro_price(int pro_price) {
		this.pro_price = pro_price;
	}
	public int getHot_recommand() {
		return hot_recommand;
	}
	public void setHot_recommand(int hot_recommand) {
		this.hot_recommand = hot_recommand;
	}
	public int getProduct_off() {
		return product_off;
	}
	public void setProduct_off(int product_off) {
		this.product_off = product_off;
	}
	public String getProduct_up_date() {
		return product_up_date;
	}
	public void setProduct_up_date(String product_up_date) {
		this.product_up_date = product_up_date;
	}
	public String getProduct_off_date() {
		return product_off_date;
	}
	public void setProduct_off_date(String product_off_date) {
		this.product_off_date = product_off_date;
	}
	
	
	
}
